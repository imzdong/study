;8086cpu
assume cs:helloAsm
helloAsm segment
	mov ax,2000H
	mov ss,ax
	mov sp,0
	add sp,10
	pop ax
	pop bx
	push ax
	push bx
	pop ax
	pop bx

	;program return
	mov ax,4c00H
	int 21H
helloAsm ends
end

